#include "minitalk.h"

int main(int argc, char **argv)
{
	int pid;

	if (argc == 3)
	{
		pid = ft_atoi(argv[1]);
		while(1)
		{
			kill(pid, SIGUSR1);
			sleep (10);
		}
	}
	return (0);
}

